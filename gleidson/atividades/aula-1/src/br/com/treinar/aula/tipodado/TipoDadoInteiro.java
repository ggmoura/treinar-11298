package br.com.treinar.aula.tipodado;

public class TipoDadoInteiro {

	public static void main(String[] args) {
		byte idade = 127;
		System.out.println("Byte: " + idade);
		
		short s = 10000;
		System.out.println("Short: " + s);
		
		int numero = 2000000;
		System.out.println("Numero: " + numero);
		
		long codigo = 654654645;
		System.out.println("Long: " + codigo);
		
		long cpf = 555_462;
		System.out.println("Long: " + cpf);
		
	}
}
