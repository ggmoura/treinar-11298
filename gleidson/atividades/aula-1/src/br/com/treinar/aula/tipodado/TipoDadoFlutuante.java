package br.com.treinar.aula.tipodado;

public class TipoDadoFlutuante {

	public static void main(String[] args) {
		float preco = (float) 3.5;
		float preco1 = 3.6F;
		float preco2 = 3.7f;
		System.out.println("Preço: " + preco);
		System.out.println("Preço 1: " + preco1);
		System.out.println("Preço 2: " + preco2);
		
		
		double numeroGrande = 1.998;
		System.out.println("Numero Grande: " + numeroGrande);
	}

}
