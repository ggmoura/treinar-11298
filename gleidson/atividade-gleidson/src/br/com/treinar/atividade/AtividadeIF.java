package br.com.treinar.atividade;

public class AtividadeIF {

	public static void main(String[] args) {
		int numero = -1;
		
		if (numero == 1) {
			System.out.println("UM");
		} else if (numero == 2) {
			System.out.println("DOIS");
		} else if (numero == 3) {
			System.out.println("TRES");
		} else {
			System.out.println("NUMERO INVALIDO");
		}

	}
}
