package br.com.treinar.aula1;
	
public class TipoDadoFracionado {
	public static void main(String[] args) {
		float preco = (float) 3.5;
		float preco1 = 4.555F;
		float preco2 = 5.5f;
		System.out.println("Preco " + preco);
		System.out.println("Preco1 " + preco1);
		System.out.println("Preco2 " + preco2);
				
		double numeroGrande = 1.988899;
		System.out.println("Numero grande " + numeroGrande);
	}
}
