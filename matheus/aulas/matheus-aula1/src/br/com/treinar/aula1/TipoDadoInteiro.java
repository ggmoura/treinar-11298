package br.com.treinar.aula1;

public class TipoDadoInteiro {
	public static void main(String[] args) {
		byte idade = 127;
		System.out.println("byte: " + idade);
		
		short s = 10000;
		System.out.println("Short: " + s);
		
		int numero = 2000000000;
		System.out.println("Numero: " + numero );
		
		long codigo = 546546454;
		System.out.println("Long " + codigo);	
		
	}
}
